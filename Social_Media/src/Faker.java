	public static String getRandomEmail(String firstName, String lastName){
		String beginning = "";
		String middle = "@";
		String end = "gmail.com";
		
		Random rand = new Random();
		
		firstName = firstName.toLowerCase();
		firstName = firstName.trim();
		lastName = lastName.toLowerCase();
		lastName = lastName.trim();
		
		for(int i = 0; i < firstName.length(); i++){
			int  n = rand.nextInt(2) + 1;
			if(n == 2){
				if(firstName.substring(i, i+1).equals("s")){
					beginning += "$";
				}
				else if(firstName.substring(i, i+1).equals("o")){
					beginning += "0";
				}
				else{
					int m = rand.nextInt(2) + 1;
					if(m == 2){
						beginning += firstName.substring(i,i+1).toUpperCase();
					}
				}
			}
			if(n == 1){
				if(firstName.substring(i, i+1).equals("s")){
					beginning += "$";
				}
				else if(firstName.substring(i, i+1).equals("o")){
					beginning += "0";
				}
				else{
					int m = rand.nextInt(2) + 1;
					if(m == 1){
						beginning += firstName.substring(i,i+1);
					}
				}
			}
		}

		int  numberTrail = rand.nextInt(99999) + 1;
		beginning += numberTrail;
		
		return beginning + middle + end;
	}
	
	
	
